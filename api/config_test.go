func TestBuildTagPrefixMap(t *testing.T) {
	modules := []*ServiceConfig{
		{
			APIs: []string{
				"saturn.identity.v1.Identity",
			},
		},
		{
			APIs: []string{
				"saturn.identity.admin.v1.AdminIdentity",
			},
		},
	}

	tagMap := buildTagPrefixMap(modules)

	if prefix, ok := tagMap["Identity"]; !ok || prefix != "saturn.identity.v1" {
		t.Errorf("expected tag Identity to map to saturn.identity.v1, got %q (ok=%v)", prefix, ok)
	}

	if prefix, ok := tagMap["AdminIdentity"]; !ok || prefix != "saturn.identity.admin.v1" {
		t.Errorf("expected tag AdminIdentity to map to saturn.identity.admin.v1, got %q (ok=%v)", prefix, ok)
	}
}

func TestToFQN(t *testing.T) {
	tagMap := map[string]string{
		"Identity":      "saturn.identity.v1",
		"AdminIdentity": "saturn.identity.admin.v1",
	}

	tests := []struct {
		tag         string
		operationID string
		expected    string
	}{
		{
			tag:         "Identity",
			operationID: "Identity_LoginUser",
			expected:    "saturn.identity.v1.Identity.LoginUser",
		},
		{
			tag:         "Identity",
			operationID: "Identity_RegisterUser",
			expected:    "saturn.identity.v1.Identity.RegisterUser",
		},
		{
			tag:         "AdminIdentity",
			operationID: "AdminIdentity_ListUsers",
			expected:    "saturn.identity.admin.v1.AdminIdentity.ListUsers",
		},
		{
			tag:         "Unknown",
			operationID: "Identity_LoginUser",
			expected:    "",
		},
	}

	for _, tc := range tests {
		t.Run(tc.operationID, func(t *testing.T) {
			fqn := toFQN(tc.tag, tc.operationID, tagMap)
			if fqn != tc.expected {
				t.Errorf("expected %q, got %q", tc.expected, fqn)
			}
		})
	}
}

func TestApplyConfig(t *testing.T) {
	// Sample raw swagger specification generated by protoc-gen-openapiv2
	spec := []byte(`{
		"swagger": "2.0",
		"info": {
			"title": "Old Title",
			"version": "version not set"
		},
		"tags": [
			{"name": "Identity"},
			{"name": "AdminIdentity"}
		],
		"paths": {
			"/v1/identity/users:login": {
				"post": {
					"operationId": "Identity_LoginUser",
					"tags": ["Identity"],
					"security": [{"bearerAuth": []}]
				}
			},
			"/v1/identity/users:register": {
				"post": {
					"operationId": "Identity_RegisterUser",
					"tags": ["Identity"],
					"security": [{"bearerAuth": []}]
				}
			},
			"/v1/identity/sessions:refresh": {
				"post": {
					"operationId": "Identity_RefreshSession",
					"tags": ["Identity"]
				}
			},
			"/v1/admin/identity/users": {
				"get": {
					"operationId": "AdminIdentity_ListUsers",
					"tags": ["AdminIdentity"]
				}
			}
		}
	}`)

	result, err := ApplyConfig(spec)
	if err != nil {
		t.Fatalf("ApplyConfig failed: %v", err)
	}

	var doc struct {
		Info struct {
			Title       string `json:"title"`
			Description string `json:"description"`
		} `json:"info"`
		Tags []struct {
			Name        string `json:"name"`
			Description string `json:"description"`
		} `json:"tags"`
		Paths map[string]map[string]struct {
			Security      []map[string]interface{} `json:"security"`
			XAccessLevels []string                 `json:"x-access-levels"`
		} `json:"paths"`
	}

	if err := json.Unmarshal(result, &doc); err != nil {
		t.Fatalf("failed to unmarshal output spec: %v", err)
	}

	// Verify title is overridden
	if doc.Info.Title != "Saturn API" {
		t.Errorf("expected info.title to be overridden to 'Saturn API', got %q", doc.Info.Title)
	}

	// Verify description is overridden
	expectedDesc := "Saturn personal life operating system"
	if doc.Info.Description != expectedDesc {
		t.Errorf("expected info.description %q, got %q", expectedDesc, doc.Info.Description)
	}

	// Verify tag descriptions are enriched
	if len(doc.Tags) != 2 {
		t.Errorf("expected 2 tags, got %d", len(doc.Tags))
	} else {
		for _, tag := range doc.Tags {
			if tag.Name == "Identity" {
				expectedDesc := "Provides user authentication, registration, session management, and logout capabilities."
				if tag.Description != expectedDesc {
					t.Errorf("expected Identity tag description %q, got %q", expectedDesc, tag.Description)
				}
			} else if tag.Name == "AdminIdentity" {
				expectedDesc := "Provides user administration capabilities, including approving, rejecting, suspending, and role updates."
				if tag.Description != expectedDesc {
					t.Errorf("expected AdminIdentity tag description %q, got %q", expectedDesc, tag.Description)
				}
			}
		}
	}

	// Verify public endpoints have security removed
	loginOp := doc.Paths["/v1/identity/users:login"]["post"]
	if len(loginOp.Security) > 0 {
		t.Errorf("expected login to be public (no security block), got %v", loginOp.Security)
	}

	registerOp := doc.Paths["/v1/identity/users:register"]["post"]
	if len(registerOp.Security) > 0 {
		t.Errorf("expected register to be public (no security block), got %v", registerOp.Security)
	}

	// Verify public endpoints have security removed (e.g. refresh)
	refreshOp := doc.Paths["/v1/identity/sessions:refresh"]["post"]
	if len(refreshOp.Security) > 0 {
		t.Errorf("expected refresh to be public (no security block), got %v", refreshOp.Security)
	}

	// Verify admin endpoints have security injected AND x-access-levels set to ["admin"]
	adminOp := doc.Paths["/v1/admin/identity/users"]["get"]
	if len(adminOp.Security) == 0 {
		t.Errorf("expected admin endpoint to have security injected")
	}
	if len(adminOp.XAccessLevels) != 1 || adminOp.XAccessLevels[0] != "admin" {
		t.Errorf("expected admin endpoint to have x-access-levels=['admin'], got %v", adminOp.XAccessLevels)
	}
}
