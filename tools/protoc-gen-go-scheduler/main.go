package main

import (
	"context"
	"encoding/json"
	"fmt"
	"path/filepath"
	"time"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"
	"google.golang.org/protobuf/reflect/protoreflect"
	"google.golang.org/protobuf/reflect/protoregistry"
	"google.golang.org/protobuf/types/descriptorpb"
	"google.golang.org/protobuf/types/dynamicpb"
)

// Declare packages used in generated code (avoids unused imports if none generated)
var (
	_ = context.Background
	_ = json.Marshal
	_ = time.Now
)

func main() {
	protogen.Options{}.Run(func(gen *protogen.Plugin) error {
		// Build the extension registry dynamically from all files
		var types protoregistry.Types
		for _, f := range gen.Files {
			registerExtensions(&types, f.Desc)
		}

		for _, f := range gen.Files {
			if !f.Generate {
				continue
			}
			generateFile(gen, f, &types)
		}
		return nil
	})
}

func registerExtensions(types *protoregistry.Types, desc protoreflect.Descriptor) {
	if fd, ok := desc.(protoreflect.FileDescriptor); ok {
		exts := fd.Extensions()
		for i := 0; i < exts.Len(); i++ {
			_ = types.RegisterExtension(dynamicpb.NewExtensionType(exts.Get(i)))
		}
		msgs := fd.Messages()
		for i := 0; i < msgs.Len(); i++ {
			registerExtensions(types, msgs.Get(i))
		}
	} else if md, ok := desc.(protoreflect.MessageDescriptor); ok {
		exts := md.Extensions()
		for i := 0; i < exts.Len(); i++ {
			_ = types.RegisterExtension(dynamicpb.NewExtensionType(exts.Get(i)))
		}
		msgs := md.Messages()
		for i := 0; i < msgs.Len(); i++ {
			registerExtensions(types, msgs.Get(i))
		}
	}
}

type jobMessage struct {
	GoIdent protogen.GoIdent
	JobType string
}

func generateFile(gen *protogen.Plugin, f *protogen.File, types *protoregistry.Types) {
	var jobs []jobMessage

	// Scan messages for the job_type extension
	for _, msg := range f.Messages {
		jobType := getJobType(msg, types)
		if jobType != "" {
			jobs = append(jobs, jobMessage{
				GoIdent: msg.GoIdent,
				JobType: jobType,
			})
		}
	}

	if len(jobs) == 0 {
		return
	}

	filename := f.GeneratedFilenamePrefix + ".pb.scheduler.go"
	g := gen.NewGeneratedFile(filename, f.GoImportPath)

	g.P("// Code generated by protoc-gen-go-scheduler. DO NOT EDIT.")
	g.P("// Source: ", filepath.Base(f.Desc.Path()))
	g.P()
	g.P("package ", f.GoPackageName)
	g.P()

	g.P("import (")
	g.P(`	"context"`)
	g.P(`	"encoding/json"`)
	g.P(`	"time"`)
	g.P()
	g.P(`	"github.com/masterkeysrd/saturn/internal/platform/scheduler"`)
	g.P(")")
	g.P()

	for _, job := range jobs {
		typeName := job.GoIdent.GoName

		// 1. Handler signature
		g.P("// ", typeName, "Handler is the strongly-typed callback signature for the '", job.JobType, "' job.")
		g.P("type ", typeName, "Handler func(ctx context.Context, payload *", typeName, ") error")
		g.P()

		// 2. Register helper
		g.P("// Register", typeName, " binds the handler callback to the scheduler engine.")
		g.P("func Register", typeName, "(engine *scheduler.Engine, handler ", typeName, "Handler) {")
		g.P("	engine.Register(", fmt.Sprintf("%q", job.JobType), ", func(ctx context.Context, payloadBytes []byte) error {")
		g.P("		var payload ", typeName)
		g.P("		if err := json.Unmarshal(payloadBytes, &payload); err != nil {")
		g.P("			return err")
		g.P("		}")
		g.P("		return handler(ctx, &payload)")
		g.P("	})")
		g.P("}")
		g.P()

		// 3. Enqueue option struct
		g.P("// ", typeName, "Job represents the enqueue request options for '", job.JobType, "'.")
		g.P("type ", typeName, "Job struct {")
		g.P("	Payload     *", typeName)
		g.P("	RunAt       time.Time")
		g.P("	MaxAttempts int")
		g.P("}")
		g.P()

		// 4. Enqueue helper
		g.P("// Enqueue", typeName, " puts the job on the queue with compile-time type safety.")
		g.P("func Enqueue", typeName, "(ctx context.Context, sched scheduler.Scheduler, job ", typeName, "Job) error {")
		g.P("	return sched.Enqueue(ctx, scheduler.Job{")
		g.P("		JobType:     ", fmt.Sprintf("%q", job.JobType), ",")
		g.P("		RunAt:       job.RunAt,")
		g.P("		Payload:     job.Payload,")
		g.P("		MaxAttempts: job.MaxAttempts,")
		g.P("	})")
		g.P("}")
		g.P()
	}
}

func getJobType(msg *protogen.Message, types *protoregistry.Types) string {
	optionsProto := msg.Desc.Options().(*descriptorpb.MessageOptions)
	raw, err := proto.Marshal(optionsProto)
	if err != nil {
		return ""
	}

	var resolvedOptions descriptorpb.MessageOptions
	err = proto.UnmarshalOptions{Resolver: types}.Unmarshal(raw, &resolvedOptions)
	if err != nil {
		return ""
	}

	var jobType string
	resolvedOptions.ProtoReflect().Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		// Field number 50001 corresponds to our extend string job_type option
		if fd.Number() == 50001 {
			jobType = v.String()
			return false // stop range
		}
		return true
	})
	return jobType
}
